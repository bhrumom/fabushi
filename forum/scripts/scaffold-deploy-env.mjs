#!/usr/bin/env node

import { access, writeFile } from "node:fs/promises";
import { constants as fsConstants } from "node:fs";
import { dirname, resolve } from "node:path";
import { spawn } from "node:child_process";
import { fileURLToPath } from "node:url";

function parseArgs(argv) {
  const parsed = {
    preset: "read-only-preview",
    "deploy-env-path": ".env.deploy",
    "forum-image": "ghcr.io/bhrumom/fabushi-forum:main",
    "forum-port": "3000",
    "forum-data-dir": "./data",
    "data-source": "sqlite",
    "writes-enabled": "",
    "write-access-code": "",
    "deployment-stage": "",
    "public-base-url": "",
    force: "false",
  };

  for (let index = 0; index < argv.length; index += 1) {
    const part = argv[index];

    if (!part.startsWith("--")) {
      throw new Error(`Unexpected argument: ${part}`);
    }

    const key = part.slice(2);
    const value = argv[index + 1];

    if (value === undefined || value.startsWith("--")) {
      throw new Error(`Missing value for --${key}`);
    }

    parsed[key] = value;
    index += 1;
  }

  return parsed;
}

function parseBoolean(value, key) {
  if (value === undefined || value === null || value === "") {
    return undefined;
  }

  if (value === "true") {
    return true;
  }

  if (value === "false") {
    return false;
  }

  throw new Error(`Expected ${key} to be true or false, received: ${value}`);
}

function normalizeUrl(value) {
  return value ? value.replace(/\/$/, "") : "";
}

function quoteEnvValue(value) {
  if (value === "") {
    return "";
  }

  if (/^[A-Za-z0-9._/:=-]+$/.test(value)) {
    return value;
  }

  return `"${value.replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"`;
}

function buildConfig(args) {
  const preset = args.preset?.trim() || "read-only-preview";
  if (!["read-only-preview", "writable-preview", "production"].includes(preset)) {
    throw new Error(
      `Expected --preset to be read-only-preview, writable-preview, or production, received: ${preset}`,
    );
  }

  const deploymentStageOverride = args["deployment-stage"]?.trim() || "";
  const writesEnabledOverride = parseBoolean(args["writes-enabled"], "writes_enabled");
  const publicBaseUrlOverride = normalizeUrl(args["public-base-url"]?.trim() || "");
  const writeAccessCode = args["write-access-code"] ?? "";
  const dataSource = args["data-source"]?.trim() || "sqlite";

  if (dataSource !== "sqlite" && dataSource !== "seed-json") {
    throw new Error(`Expected --data-source to be sqlite or seed-json, received: ${dataSource}`);
  }

  const presetDefaults = {
    "read-only-preview": {
      deploymentStage: "preview",
      publicBaseUrl: "",
      writesEnabled: false,
      requirePublicBaseUrl: false,
      requireWriteAccessCode: false,
    },
    "writable-preview": {
      deploymentStage: "preview",
      publicBaseUrl: "",
      writesEnabled: true,
      requirePublicBaseUrl: false,
      requireWriteAccessCode: true,
    },
    production: {
      deploymentStage: "production",
      publicBaseUrl: "",
      writesEnabled: false,
      requirePublicBaseUrl: true,
      requireWriteAccessCode: false,
    },
  };

  const defaults = presetDefaults[preset];
  const deploymentStage = deploymentStageOverride || defaults.deploymentStage;
  if (deploymentStage !== "preview" && deploymentStage !== "production") {
    throw new Error(`Expected --deployment-stage to be preview or production, received: ${deploymentStage}`);
  }

  const publicBaseUrl = publicBaseUrlOverride || defaults.publicBaseUrl;
  const writesEnabled = writesEnabledOverride ?? defaults.writesEnabled;

  if (defaults.requirePublicBaseUrl && !publicBaseUrl) {
    throw new Error(`--preset ${preset} requires --public-base-url.`);
  }

  if (defaults.requireWriteAccessCode && !writeAccessCode.trim()) {
    throw new Error(`--preset ${preset} requires --write-access-code.`);
  }

  if (deploymentStage === "production" && writesEnabled) {
    throw new Error("Refusing to scaffold a writable production deploy env. Use preview for write validation first.");
  }

  return {
    preset,
    deployEnvPath: args["deploy-env-path"],
    forumImage: args["forum-image"]?.trim() || "ghcr.io/bhrumom/fabushi-forum:main",
    forumPort: args["forum-port"]?.trim() || "3000",
    forumDataDir: args["forum-data-dir"]?.trim() || "./data",
    dataSource,
    deploymentStage,
    publicBaseUrl,
    writesEnabled,
    writeAccessCode: writeAccessCode.trim(),
    force: parseBoolean(args.force, "force") ?? false,
  };
}

function renderDeployEnv(config) {
  return [
    "# Generated by forum/scripts/scaffold-deploy-env.mjs",
    `# Preset: ${config.preset}`,
    `FORUM_IMAGE=${quoteEnvValue(config.forumImage)}`,
    `FORUM_PORT=${quoteEnvValue(config.forumPort)}`,
    `FORUM_DATA_DIR=${quoteEnvValue(config.forumDataDir)}`,
    `FORUM_DEPLOYMENT_STAGE=${config.deploymentStage}`,
    `FORUM_PUBLIC_BASE_URL=${quoteEnvValue(config.publicBaseUrl)}`,
    `FORUM_DATA_SOURCE=${config.dataSource}`,
    `FORUM_ENABLE_WRITES=${String(config.writesEnabled)}`,
    `FORUM_WRITE_ACCESS_CODE=${quoteEnvValue(config.writeAccessCode)}`,
    "",
  ].join("\n");
}

async function ensureWritableTarget(config) {
  if (config.force) {
    return;
  }

  try {
    await access(config.deployEnvPath, fsConstants.F_OK);
  } catch {
    return;
  }

  throw new Error(
    `Refusing to overwrite existing deploy env at ${config.deployEnvPath}. Re-run with --force true if you want to replace it.`,
  );
}

function runValidateScript(deployEnvPath) {
  const scriptDir = dirname(fileURLToPath(import.meta.url));
  const validateScriptPath = resolve(scriptDir, "validate-deploy-env.mjs");

  return new Promise((resolvePromise, rejectPromise) => {
    const child = spawn(process.execPath, [validateScriptPath, "--deploy-env-path", deployEnvPath], {
      stdio: "inherit",
      env: process.env,
    });

    child.on("error", rejectPromise);
    child.on("exit", (code, signal) => {
      if (signal) {
        rejectPromise(new Error(`validate-deploy-env.mjs exited via signal: ${signal}`));
        return;
      }

      if (code !== 0) {
        rejectPromise(new Error(`validate-deploy-env.mjs exited with code ${code}.`));
        return;
      }

      resolvePromise();
    });
  });
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const config = buildConfig(args);
  await ensureWritableTarget(config);

  const renderedEnv = renderDeployEnv(config);
  await writeFile(config.deployEnvPath, renderedEnv, "utf-8");

  console.log(`Wrote ${config.deployEnvPath}`);
  await runValidateScript(config.deployEnvPath);

  const deployEnvPath = config.deployEnvPath;
  const forumUrlHint =
    config.deploymentStage === "production" && config.publicBaseUrl
      ? config.publicBaseUrl
      : "https://forum-preview.example.com";

  console.log("");
  console.log("## Next steps");
  console.log("");
  console.log(`- Review ${deployEnvPath} and adjust any host-specific values if needed.`);
  console.log(`- docker compose --env-file ${deployEnvPath} -f docker-compose.deploy.yml up -d`);
  console.log(`- pnpm smoke:deploy-env -- --forum-url ${forumUrlHint} --deploy-env-path ${deployEnvPath}`);
  console.log(`- pnpm handoff:live-target -- --forum-url ${forumUrlHint} --deploy-env-path ${deployEnvPath}`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exitCode = 1;
});